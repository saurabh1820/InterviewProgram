package swapping;

public class Swapping {

	//with third variable
	public static void main(String[] args) {
		
		int a=2;
		int b=3;
		int temp;
		System.out.println("A : "+a);
		System.out.println("B : "+b);
		temp=a;
		a=b;
		b=temp;
		System.out.println("A : "+a);
		System.out.println("B : "+b);

	}

}
